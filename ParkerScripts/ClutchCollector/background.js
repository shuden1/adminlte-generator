chrome.runtime.onMessage.addListener(function(request, sender, sendResponse) {
    if (request.action == "start") {
        openTab(urls);
    } else if (request.action == "download") {
        downloadResults();
    }
});
function openTab(urls) {
  if (urls.length === 0) return;

  var url = urls.pop();

  chrome.tabs.create({url: url, active: true}, function(tab) {
    // Add a delay before executing the script
    setTimeout(function() {
        chrome.tabs.executeScript(tab.id, {
            code:
                `var element = document.querySelector('.profile-quick-menu__visit-website a');
                 element ? element.href : null;`
        }, function(results) {
            if (results[0]) {
                console.log('URL:', url, 'Href:', results[0]);

                // Save the data to Chrome storage
                chrome.storage.local.get(['clutchURLs'], function(result) {
                    var clutchURLs = result.clutchURLs ? result.clutchURLs : [];
                    clutchURLs.push({url: url, href: results[0]});
                    chrome.storage.local.set({clutchURLs: clutchURLs}, function() {
                        console.log('Data saved');
                    });
                });

                chrome.tabs.remove(tab.id, function() {
                    openTab(urls);
                });
            }
        });
    }, 5000); // Wait for 5 seconds
  });
}
function downloadResults() {
    // Retrieve the data from Chrome's local storage
    chrome.storage.local.get(['clutchURLs'], function(result) {
        var clutchURLs = result.clutchURLs ? result.clutchURLs : [];

        // Prepare CSV data
        var csvContent = "data:text/csv;charset=utf-8,";
        csvContent += ["URL", "Href"].join(",") + "\n";

        clutchURLs.forEach(function(item) {
            csvContent += [item.url, item.href].join(",") + "\n";
        });

        // Create a Blob object from the CSV string
        var blob = new Blob([csvContent], {type: "text/csv"});

        // Create a URL for the Blob object
        var url = URL.createObjectURL(blob);

        // Create a temporary anchor element
        var a = document.createElement('a');
        a.href = url;

        // Set the download attribute of the anchor element to the desired filename
        a.download = 'clutchURLs.csv';

        // Programmatically click the anchor element to start the download
        a.click();

        // Clean up by revoking the Blob URL and removing the anchor element
        setTimeout(function() {
            URL.revokeObjectURL(url);
            a.remove();
        }, 0);
    });
}
